

let count = prompt('enter your number?'); 

// if count == 0 -> hey 
// count == 1 -> boo 
// count == 2 -> word 
// else  -> tttt

if (count == 0) {
    console.log('hey');
}

else if (count == 1) {
    console.log('boo');
}

else if (count == 2) {
    console.log('word');
}

else {
    console.log('ttt');
}


